function getXYZ(arr: [][][]) {
  let len = arr.length;
  let x = 0;
  let y = 0;
  let z = 0;

  let minX = len;
  let maxX = 0;
  let minZ = len;
  let maxZ = 0;
  for (let i = 0; i < len; i++) {
    if (!arr[i] || !arr[i].length) continue;
    y++;
    for (let j = 0; j < len; j++) {
      if (!arr[i][j] || !arr[i][j].length) continue;
      minX = Math.min(minX, j);
      maxX = Math.max(maxX, j);
      for (let k = 0; k < len; k++) {
        let item = arr[i][j][k];
        if (item) {
          minZ = Math.min(minZ, k);
          maxZ = Math.max(maxZ, k);
        }
      }
    }
  }

  x = maxX - minX + 1;
  z = maxZ - minZ + 1;

  return { x, y, z };
}

